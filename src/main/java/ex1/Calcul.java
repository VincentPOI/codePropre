public class Calcul {

	/**
	 * Méthode qui permet d'additionner les entiers a et b
	 * 
	 * @param a paramètre a
	 * @param b paramètre b
	 * @return int
	 */
	public int additionner(int a, int b){
		return a+b;
	}
	
	/**
	 * Mèthode qui permet de soustraire les entiers a et b
	 * 
	 * @param a paramètre a
	 * @param b paramètre b
	 * @return int
	 */
	public int soustraire(int a, int b){
		return a-b;
	}
}
